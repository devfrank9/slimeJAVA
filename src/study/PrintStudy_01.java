package study;
public class PrintStudy_01 {
    public static void main(String[] args) {
        System.out.println("안녕 자바!"); //print 뒤에 ln이 붙는 경우는 출력 후 줄바꿈을 위한것
        System.out.print("아버지가"); //다음과 같이 print 만 선언할 경우 뒤에 나오는 선언문과 붙어져서 출력된다.
        System.out.print("방에 들어가신다\n"); // 출력결과 '아버지가방에 들어가신다'

        //실습
        System.out.println("안");
        System.out.println("녕");
        System.out.println("자");
        System.out.println("바");
    }   
}